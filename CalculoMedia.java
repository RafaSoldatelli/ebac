package TarefaCalculoMedia;

public class CalculoMedia {

    public static void main(String[] args) {

        int nota1 = 2;
        int nota2 = 3;
        int nota3 = 6;
        int nota4 = 8;

        int soma = nota1 + nota2 + nota3 + nota4;

        double media = soma / 4.0;

        System.out.println("A média das notas é: " + media);
    }
}
